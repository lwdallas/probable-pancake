func DatabaseConnectionShowV1(w http.ResponseWriter, r *http.Request) {
	vars := mux.Vars(r)
	var databaseConnectionId int
	var err error
	if databaseConnectionId, err = strconv.Atoi(vars["databaseConnectionId"]); err != nil {
		panic(err)
	}
	databaseConnection := RepoFindDatabaseConnection(databaseConnectionId)
	if databaseConnection.Id > 0 {
		w.Header().Set("Content-Type", "application/json; charset=UTF-8")
		w.WriteHeader(http.StatusOK)
		if err := json.NewEncoder(w).Encode(databaseConnection); err != nil {
			panic(err)
		}
		return
	}

	// If we didn't find it, 404
	w.Header().Set("Content-Type", "application/json; charset=UTF-8")
	w.WriteHeader(http.StatusNotFound)
	if err := json.NewEncoder(w).Encode(jsonErr{Code: http.StatusNotFound, Text: "Not Found"}); err != nil {
		panic(err)
	}

}

/*
Test with this curl command:

curl -H "Content-Type: application/json" -d '{"name":"New DatabaseConnection"}' http://localhost:8080/v1/databaseconnections

*/
func DatabaseConnectionCreateV1(w http.ResponseWriter, r *http.Request) {
	var databaseConnection DatabaseConnection
	body, err := ioutil.ReadAll(io.LimitReader(r.Body, 1048576))
	if err != nil {
		panic(err)
	}
	if err := r.Body.Close(); err != nil {
		panic(err)
	}
	if err := json.Unmarshal(body, &databaseConnection); err != nil {
		w.Header().Set("Content-Type", "application/json; charset=UTF-8")
		w.WriteHeader(422) // unprocessable entity
		if err := json.NewEncoder(w).Encode(err); err != nil {
			panic(err)
		}
	}

	t := RepoCreateDatabaseConnection(databaseConnection)
	w.Header().Set("Content-Type", "application/json; charset=UTF-8")
	w.WriteHeader(http.StatusCreated)
	if err := json.NewEncoder(w).Encode(t); err != nil {
		panic(err)
	}
}
